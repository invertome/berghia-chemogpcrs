# --- Helper function to find first available image ---
find_image() {
    local dir="$1"
    local pattern="$2"
    local fallback="$3"

    for f in "${dir}"/${pattern}; do
        if [ -f "$f" ]; then
            echo "$f"
            return 0
        fi
    done
    echo "$fallback"
    return 1
}

# --- Helper function to count lines in file ---
count_lines() {
    local file="$1"
    if [ -f "$file" ]; then
        wc -l < "$file" | tr -d ' '
    else
        echo "0"
    fi
}

# --- Helper function to safely read CSV value ---
read_csv_value() {
    local file="$1"
    local col="$2"
    local row="${3:-2}"  # Default to second row (first data row)
    if [ -f "$file" ]; then
        awk -F',' -v col="$col" -v row="$row" 'NR==1{for(i=1;i<=NF;i++)if($i==col)c=i} NR==row&&c{print $c}' "$file"
    fi
}

